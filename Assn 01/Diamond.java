// Desc:	This simple Java program prints a diamond shape to the screen

public class Diamond {

	public static void main(String[] args) {
		
		// Print spaces and asterisks to the screen to form a diamond
		System.out.println("     *");
		System.out.println("    ***");
		System.out.println("   *****");
		System.out.println("  *******");
		System.out.println(" *********");
		System.out.println("  *******");
		System.out.println("   *****");
		System.out.println("    ***");
		System.out.println("     *");

	}

}
